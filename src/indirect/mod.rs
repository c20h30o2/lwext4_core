//! Indirect blocks 模块
//!
//! 实现 ext2/ext3 传统的间接块寻址方式，用于不使用 extent 的 inode。
//!
//! ## 间接块层级
//!
//! - **直接块** (0-11): 直接存储在 inode.i_block[0..12] 中
//! - **一级间接** (12-1035): inode.i_block[12] -> 间接块 -> 数据块
//! - **二级间接** (1036-1049611): inode.i_block[13] -> 间接块 -> 间接块 -> 数据块
//! - **三级间接** (1049612+): inode.i_block[14] -> 间接块 -> 间接块 -> 间接块 -> 数据块
//!
//! ## 计算公式
//!
//! 假设块大小为 4096 字节，每个间接块可以存储 1024 个指针（4096 / 4 = 1024）：
//!
//! - 直接块范围: [0, 12)
//! - 一级间接: [12, 12 + 1024) = [12, 1036)
//! - 二级间接: [1036, 1036 + 1024*1024) = [1036, 1049612)
//! - 三级间接: [1049612, 1049612 + 1024*1024*1024)

